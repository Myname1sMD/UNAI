function renderQuestion() {
    const container = document.getElementById('quiz-container');
    const translatedQuiz = getTranslatedQuiz();
    const q = translatedQuiz[currentQuestionIndex];
    
    if (!q) {
        analyzeResults(); 
        return;
    }

    const questionLabel = translations[currentLanguage]['quiz.questionLabel'] || '–í–æ–ø—Ä–æ—Å';
    let htmlContent = `<h4>${questionLabel} ${currentQuestionIndex + 1}/${translatedQuiz.length}: ${q.question}</h4>`;
    
    q.options.forEach((option) => {
        // –≠–∫—Ä–∞–Ω–∏—Ä—É–µ–º JSON –¥–ª—è –ø–µ—Ä–µ–¥–∞—á–∏ –≤ —Ñ—É–Ω–∫—Ü–∏—é
        const scoreString = JSON.stringify(option.score).replace(/"/g, '&quot;');
        htmlContent += `<button onclick='processAnswer(${scoreString})'>${option.text}</button>`;
    });

    container.innerHTML = htmlContent;
}

function processAnswer(scores) {
    for (const key in scores) {
        if (profileScores.hasOwnProperty(key)) {
            profileScores[key] += scores[key];
        }
    }

    currentQuestionIndex++;
    const translatedQuiz = getTranslatedQuiz();
    if (currentQuestionIndex < translatedQuiz.length) {
        renderQuestion();
    } else {
        analyzeResults();
    }
}

